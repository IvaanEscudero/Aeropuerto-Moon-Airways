    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        
        Usuario usuarioalt = usuario.login("cliente", "");

        if (usuarioalt != null) {
            Class tipoUsuario = usuarioalt.getClass();

            if (tipoUsuario == Cliente.class) {
                    SelecionOrigDestCliente mencliente = new SelecionOrigDestCliente((Cliente) usuarioalt, this);
                    mencliente.setVisible(true);                    
            }
            this.setVisible(false);
            reset();
        }
        else {
            usuarioalt = usuarioalt.crearCliente();
            SelecionOrigDestCliente mencliente = new SelecionOrigDestCliente((Cliente) usuarioalt, this);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String user = jTextField1.getText();
        String pass = new String(jPasswordField1.getPassword());
        Usuario usuarioalt = usuario.login(user, pass);


        if (usuarioalt != null) {
            Class tipoUsuario = usuarioalt.getClass();

            if (tipoUsuario == Supervisor.class) {
                    MenuPrincipalSupervisor supervisor = new MenuPrincipalSupervisor((Supervisor)usuarioalt, this);
                    supervisor.setVisible(true);
            }
            else if (tipoUsuario == Empleado.class) {
                    MenuPersonalTierra pertierra = new MenuPersonalTierra((Empleado) usuarioalt, this);
                    pertierra.setVisible(true);                    
            }
            this.setVisible(false);
            reset();
        }
        else {
            JOptionPane.showMessageDialog(this, "Usuario Invalido. Vuelva a intentarlo");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

